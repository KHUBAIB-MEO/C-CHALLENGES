// SHOW THE FOLLOWING PATTERN USING SINGLE PRINT STATEMENT //

#include <stdio.h>

int main()
{

    // RIGHT HALF PYRAMID//
    printf("*\n**\n***\n****\n*****");
    printf("\n\n");

    // REVERSE RIGHT HALF PYRAMID//
    printf("*****\n****\n***\n**\n*");
    printf("\n\n");

    // LEFT HALF PYRAMID//
    printf("    *\n   **\n  ***\n ****\n*****");

    return 0;
}