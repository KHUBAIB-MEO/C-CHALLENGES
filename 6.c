// DEFINE A VARIABLE FOR STORING A USER'S FIRST NAME, LAST NAME AND AGE USING APPROPRIATE NAMING CONVENTION AND THEN DISPLAY THEM //

#include <stdio.h>
#include <string.h>
int main()
{
    char first_name[20] = "KHUBAIB";
    char last_name[20] = "NAEEM";
    int age = 20;

    printf("First name: %s\n", first_name);
    printf("Last name: %s\n", last_name);
    printf("Age: %d", age);
    return 0;
}